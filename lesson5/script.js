// Functions Part 2
// Parameters
// Arguments

// parameters > flavour, amount
function makePancake(flavour) {
    console.log(`making ${flavour} pancake`)
}

makePancake("blueberry", 5)

// parameters > flavour, amount
function makeManyPancakes(flavour, amount) {
    console.log(`making ${amount} ${flavour} pancakes`)
}

// arguments > blueberry, apple
makeManyPancakes("apple", 4)


